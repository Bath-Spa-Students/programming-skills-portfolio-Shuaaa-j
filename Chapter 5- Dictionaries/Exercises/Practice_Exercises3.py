''' Instructions of practice exercise 5.4
Write a Python program to iterate through the values of a dictionary and print them'''

# Stores key-value pairs in the dictionary
my_dict = {
    "Name": "Joshua Gofulco",
    "Age": 17,
    "Residence": "Abu Dhabi",
}

# Iterate through the values and display them
for value in my_dict.values():
    print(value)
