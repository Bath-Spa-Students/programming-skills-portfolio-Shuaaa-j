''' Instructions for exercise 2.1
Assign a message to a variable, and print that message.
Then change the value of the variable to a new message, and print the new message.'''

# Assigns a value to the variable 'score'
score = "The score is 2-0, with France in the lead!"

# Prints the variable 'score' concatenated with a string
print("Initial message: " + score)

# Changes the value of the variable 'score' to a new message
score = "What a comeback! The score has changed to 4-2, with Argentina in the lead!"

# Prints the updated variable 'score' concatenated with a string
print("Updated message: " + score)
