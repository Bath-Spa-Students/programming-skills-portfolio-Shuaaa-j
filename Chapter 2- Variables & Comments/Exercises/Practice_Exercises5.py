'''Instructions for practice exercise 2.5
Write a python program that stores fruit names in a list named fruits. Unpack the list into three
variables x, y and z and then print the values of each variable.'''

# Stores fruit names in a list
fruits = ["Tangerines", "Coconut", "Peaches"]

# Unpacks the list into three variables
x, y, z = fruits

# Prints the values of each variable
print("x:", x)
print("y:", y)
print("z:", z)

