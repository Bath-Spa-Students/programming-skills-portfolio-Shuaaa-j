#Stores the favorite fruits in the list
favorite_fruits = ["Coconut", "Peaches", "Mango"]

# if there is a match with the items in the list, prints a statement
if "Durian" in favorite_fruits:
    print("You must love Durian!")

if "Banana" in favorite_fruits:
    print("You must love Banana!")

if "Mango" in favorite_fruits:
    print("You must love Mango!")

if "Pineapple" in favorite_fruits:
    print("You must love Pineapple!")

if "Apple" in favorite_fruits:
    print("You must love Apple!")

